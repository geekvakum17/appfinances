          <td>${t.numCrecip}</td>
          <td style="font-weight:bold; color:${estEmise ? 'red' : 'green'};">
            ${estEmise ? '-' : '+'} ${new Intl.NumberFormat("fr-FR").format(t.montant)} ${t.devise}
          </td>
          <td>${t.devise}</td>
          <td>${t.motif}</td>
          <td>${new Date(t.date).toLocaleString("fr-FR")}</td>
        </tr>
      `;
           tbody.innerHTML += row;
         });
       } catch (err) {
         console.error("Erreur :", err);
       }
     }

     // Charger au d√©marrage
     chargerTransactions();

